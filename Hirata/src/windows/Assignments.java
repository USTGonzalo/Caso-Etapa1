    private void BtnAssingsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAssingsActionPerformed
        // TODO add your handling code here:
        // Validar selección
        if (idCamion == -1 || idConductor == -1) {
            JOptionPane.showMessageDialog(this,
                    "Debe seleccionar un camión y un conductor",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        BasedeDatos bd = new BasedeDatos();
        Connection conn = bd.conectar();

        if (conn != null) {
            try {
                // Verificar si ya existe la asignación
                String checkSql = "SELECT * FROM camion_conductor WHERE id_camion = ? OR id_conductor = ?";
                PreparedStatement checkPs = conn.prepareStatement(checkSql);
                checkPs.setInt(1, idCamion);
                checkPs.setInt(2, idConductor);
                ResultSet rs = checkPs.executeQuery();

                if (rs.next()) {
                    JOptionPane.showMessageDialog(this,
                            "El camión o el conductor ya están asignados",
                            "Advertencia",
                            JOptionPane.WARNING_MESSAGE
                    );
                    return;
                }

                // Insertar asignación
                String sql = "INSERT INTO camion_conductor (id_camion, id_conductor) VALUES (?, ?)";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setInt(1, idCamion);
                ps.setInt(2, idConductor);

                int resultado = ps.executeUpdate();

                if (resultado > 0) {
                    JOptionPane.showMessageDialog(this,
                            "Asignación realizada correctamente",
                            "Éxito",
                            JOptionPane.INFORMATION_MESSAGE
                    );

                    // Limpiar campos
                    TxtTruck.setText("");
                    TxtDriver.setText("");
                    idCamion = -1;
                    idConductor = -1;

                } else {
                    JOptionPane.showMessageDialog(this,
                            "No se pudo asignar",
                            "Error",
                            JOptionPane.ERROR_MESSAGE
                    );
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                        "Error: " + e.getMessage(),
                        "Error",
                        JOptionPane.ERROR_MESSAGE
                );
            }
        }
    }//GEN-LAST:event_BtnAssingsActionPerformed

