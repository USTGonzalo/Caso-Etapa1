    private void cargarConductores() {
        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("ID");
        modelo.addColumn("Nombre");

        BasedeDatos bd = new BasedeDatos();
        Connection conn = bd.conectar();

        if (conn != null) {
            try {
                String sql = "SELECT ID_conductor, nombre FROM conductores";
                PreparedStatement ps = conn.prepareStatement(sql);
                ResultSet rs = ps.executeQuery();

                while (rs.next()) {
                    Object[] fila = new Object[2];
                    fila[0] = rs.getInt("ID_conductor");
                    fila[1] = rs.getString("nombre");

                    modelo.addRow(fila);
                }

                TblDrivers.setModel(modelo);

            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }

    private void cargarCamiones() {
        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("ID");
        modelo.addColumn("Patente");
        modelo.addColumn("Modelo");

        BasedeDatos bd = new BasedeDatos();
        Connection conn = bd.conectar();

        if (conn != null) {
            try {
                String sql = "SELECT id_camion, patente, modelo FROM camiones";
                PreparedStatement ps = conn.prepareStatement(sql);
                ResultSet rs = ps.executeQuery();

                while (rs.next()) {
                    Object[] fila = new Object[3];
                    fila[0] = rs.getInt("id_camion");
                    fila[1] = rs.getString("patente");
                    fila[2] = rs.getString("modelo");

                    modelo.addRow(fila);
                }

                TblTrucks.setModel(modelo);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                        "Error: " + e.getMessage(),
                        "Error",
                        JOptionPane.ERROR_MESSAGE
                );
            }
        }
    }

    private void TableDriversMouseClicked(java.awt.event.MouseEvent evt) {
        int fila = TblDrivers.getSelectedRow();

        if (fila >= 0) {
            idConductor = Integer.parseInt(TblDrivers.getValueAt(fila, 0).toString());
            String nombre = TblDrivers.getValueAt(fila, 1).toString();

            TxtDriver.setText(nombre);
        }
    }

    private void TbTrucksMouseClicked(java.awt.event.MouseEvent evt) {
        int fila = TblTrucks.getSelectedRow();

        if (fila >= 0) {
            idCamion = Integer.parseInt(TblTrucks.getValueAt(fila, 0).toString());
            String patente = TblTrucks.getValueAt(fila, 1).toString();
            String modelo = TblTrucks.getValueAt(fila, 2).toString();

            TxtTruck.setText(patente + " - " + modelo);
        }
    }

