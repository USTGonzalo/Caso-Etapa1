    private void cargarCamionesAdmin() {
        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("ID");
        modelo.addColumn("Patente");
        modelo.addColumn("Modelo");
        modelo.addColumn("Kilometraje");

        BasedeDatos bd = new BasedeDatos();
        Connection conn = bd.conectar();

        if (conn != null) {
            try {
                String sql = "SELECT id_camion, patente, modelo, kilometraje FROM camiones";
                PreparedStatement ps = conn.prepareStatement(sql);
                ResultSet rs = ps.executeQuery();

                while (rs.next()) {
                    Object[] fila = new Object[4];
                    fila[0] = rs.getInt("id_camion");
                    fila[1] = rs.getString("patente");
                    fila[2] = rs.getString("modelo");
                    fila[3] = rs.getInt("kilometraje");

                    modelo.addRow(fila);
                }

                TblTrucks.setModel(modelo);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                        "Error: " + e.getMessage(),
                        "Error",
                        JOptionPane.ERROR_MESSAGE
                );
            }
        }
    }
    
    private void cargarCamionesNormal()
    {
        DefaultTableModel model = new DefaultTableModel();

        model.addColumn("ID");
        model.addColumn("Patente");
        model.addColumn("Modelo");
        model.addColumn("Kilometraje");

        TblTrucks.setModel(model);

        try {
            BasedeDatos bd = new BasedeDatos();
            Connection conn = bd.conectar();

            String sql = "SELECT c.id_camion, c.patente, c.modelo, c.kilometraje "
                    + "FROM camiones c "
                    + "INNER JOIN camion_conductor cc ON c.id_camion = cc.id_camion "
                    + "WHERE cc.id_conductor = ?";

            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, userId);

            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                Object[] fila = new Object[4];
                fila[0] = rs.getInt("id_camion");
                fila[1] = rs.getString("patente");
                fila[2] = rs.getString("modelo");
                fila[3] = rs.getInt("kilometraje");

                model.addRow(fila);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar camiones: " + e.getMessage());
        }
    }

    private void TbTrucksMouseClicked(java.awt.event.MouseEvent evt) {
        int fila = TblTrucks.getSelectedRow();

        if (fila >= 0) {
            String id = TblTrucks.getValueAt(fila, 0).toString();
            String patente = TblTrucks.getValueAt(fila, 1).toString();
            String modelo = TblTrucks.getValueAt(fila, 2).toString();
            String km = TblTrucks.getValueAt(fila, 3).toString();

            TxtSelectTruck.setText(
                    "ID: " + id + " | " + patente + " - " + modelo + " | " + km + " km"
            );

            IdMantencion = Integer.parseInt(id);
        }
    }

