    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
        // TODO add your handling code here:                                 
        // Validaciones básicas
        if (IdMantencion == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un camión");
            return;
        }

        if (TxtKm.getText().isEmpty() || TxtType.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Complete los campos obligatorios");
            return;
        }

        // Obtener estado desde RadioButton
        String estado = "";
        if (True.isSelected()) {
            estado = "Completado";
        } else if (False.isSelected()) {
            estado = "Pendiente";
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un estado");
            return;
        }

        try {
            int km = Integer.parseInt(TxtKm.getText());
            String tipo = TxtType.getText();
            String descripcion = TxtDesc.getText();

            BasedeDatos bd = new BasedeDatos();
            Connection conn = bd.conectar();

            if (conn != null) {
                String sql = "INSERT INTO mantenimientos (ultimo_kilometraje, tipo, descripcion, estado, id_camion) VALUES (?, ?, ?, ?, ?)";
                PreparedStatement ps = conn.prepareStatement(sql);

                ps.setInt(1, km);
                ps.setString(2, tipo);
                ps.setString(3, descripcion);
                ps.setString(4, estado);
                ps.setInt(5, IdMantencion);

                int resultado = ps.executeUpdate();

                if (resultado > 0) {
                    JOptionPane.showMessageDialog(this, "Mantenimiento guardado correctamente");

                    // Limpiar campos
                    TxtKm.setText("");
                    TxtType.setText("");
                    TxtDesc.setText("");
                    TxtSelectTruck.setText("");
                    True.setSelected(false);
                    False.setSelected(false);
                    IdMantencion = -1;

                } else {
                    JOptionPane.showMessageDialog(this, "Error al guardar");
                }
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El kilometraje debe ser un número");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_BtnSaveActionPerformed

